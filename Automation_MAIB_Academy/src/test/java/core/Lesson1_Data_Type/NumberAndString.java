package core.Lesson1_Data_Type;

public class NumberAndString {
    public static void main(String[] args) {
        int z=5;
        int x=14;
        int suma=z+x;
        System.out.println("Integer"+" "+suma);
        String a="5";
        String b="6";
        String c=a+b;
        System.out.println("String" +" " +c);
        String e="6";
        int f=7;
        String g=e+f;
        System.out.println("String and int"+" "+g);

    }
}
