package core.Lesson1_Data_Type;

public class Numbers {
    public static void main(String[] args) {//main method
        /**Byte-can store whole numbers from -128 to 127**/
        byte nr=127;
        System.out.println(nr);
        /**Short-can store whole numbers from -32768 to 32767**/
        short nr1=32767;
        System.out.println(nr1);
        /**Int -can store whole numbers from -2147483648 to 2147483647**/
        int nr2=2147483647;
        System.out.println(nr2);
        /**Long  -can store whole numbers from -9223372036854775808 to 9223372036854775807**/
        long nr3=9223372036854775807l;
        System.out.println(nr3);
        float nr4=136858689070.760976869797905f;
        System.out.println(nr4);
        double nr5=13679690.897796890568976976;
        System.out.println(nr5);
    }
}
