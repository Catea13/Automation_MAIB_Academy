package core.Lesson1_Data_Type;

public class TypeCasting {
    public static void main(String[] args) {
        /** Type Casting:When you assign a value one from primitive data type to another type**/
        /**Example 1**/
        int nr=25;
        double doubleNr=nr;
        System.out.println(doubleNr);
        /**Example 2**/
        double doubleNr1=(double) nr;
        System.out.println(doubleNr1);

    }
        }
